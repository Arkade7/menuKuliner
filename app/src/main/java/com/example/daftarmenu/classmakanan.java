package com.example.daftarmenu;

public class classmakanan {
    private String nama, desc;
    private Integer harga, gambar;

    public classmakanan(String nama, String desc, Integer harga, Integer gambar) {
        this.nama = nama;
        this.desc = desc;
        this.harga = harga;
        this.gambar = gambar;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public Integer getHarga() {
        return harga;
    }

    public void setHarga(Integer harga) {
        this.harga = harga;
    }

    public Integer getGambar() {
        return gambar;
    }

    public void setGambar(Integer gambar) {
        this.gambar = gambar;
    }
}


